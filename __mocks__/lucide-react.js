// Create a generic icon component
const createIcon = (name) => {
  const Icon = React.forwardRef((props, ref) => 
    React.createElement('svg', {
      ...props,
      ref,
      'data-testid': `icon-${name}`,
      'aria-label': name,
    })
  );
  Icon.displayName = name;
  return Icon;
};

